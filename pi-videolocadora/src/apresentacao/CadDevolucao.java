    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        try {

            Atendimento atendimento = new Atendimento();

            //tratativa do ID
            if (!txtId.getText().equals("")) {
                atendimento.setId(Integer.parseInt(txtId.getText()));
            }

            //Se for atendimento para cliente, setar ID do cliente.
            if (radioCliente.isSelected()) {
                atendimento.setCliente_id(Integer.parseInt(txtId.getText()));
            }

            //Se for atendimento para dependente, setar ID do dependente.
            if (radioDependente.isSelected()) {
                atendimento.setDependente_id(Integer.parseInt(txtId.getText()));
            }

            atendimento.setFuncionario_id(new FuncionarioDAO().pegaId(txtAtendente.getText()));
            atendimento.setTitulo_id(new TituloDAO().pegaId(txtTitulo.getText()));
            atendimento.setQuantidadeMidias(Integer.parseInt(txtQuantidade.getText()));

            String data1 = txtDataSaida.getText();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            Date dataSaida = formato.parse(data1);
            atendimento.setDataSaida(dataSaida);

            atendimento.setStatus(false);

            atendimento.setDuracao(Integer.parseInt(txtDuracao.getText()));

            //Manipulação da mídiaS
            Midia midia = new MidiaDAO().consultarPeloTitulo(new TituloDAO().pegaId(txtTitulo.getText()), txtMidia.getText());
            midia.setId(midia.getId());
            midia.setQuantidade((midia.getQuantidade() + Integer.parseInt(txtQuantidade.getText())));
            new MidiaDAO().alterar(midia);
            atendimento.setMidia_id(midia.getId());

            //Tratamento da Data
            String data2 = txtDataEntrada.getText();
            formato = new SimpleDateFormat("dd/MM/yyyy");
            Date dataEntrada = formato.parse(data2);
            atendimento.setDataEntrada(dataEntrada);

            long diffInMillies = Math.abs(dataEntrada.getTime() - dataSaida.getTime());
            long diff = TimeUnit.DAYS.convert(diffInMillies, TimeUnit.MILLISECONDS);
            
            Integer id_multa = new MultaDAO().pegaId(comboBoxMulta.getSelectedItem().toString());
            
            atendimento.setMulta_id(id_multa);
            
            Double valor, valorMulta;
            MultaDAO perMulta = new MultaDAO();
            
            switch(comboBoxMulta.getSelectedIndex()){
                case 1:
                    valorMulta = perMulta.consultar(id_multa).getValor();
                    valor = atendimento.getValorTotal() + (diff * valorMulta);
                    atendimento.setValorTotal(valor);
                    txtValorTotal.setText(valor.toString());
                    break;
                case 2:
                    valorMulta = perMulta.consultar(id_multa).getValor();
                    valor = Double.parseDouble(txtValorTotal.getText()) + valorMulta;
                    atendimento.setValorTotal(valor);
                    txtValorTotal.setText(valor.toString());
                    break;
                case 3:
                    valorMulta = perMulta.consultar(id_multa).getValor();
                    valor = Double.parseDouble(txtValorTotal.getText()) + valorMulta;
                    atendimento.setValorTotal(valor);
                    txtValorTotal.setText(valor.toString());
                    break;
                case 4:
                    valorMulta = perMulta.consultar(id_multa).getValor();
                    valor = Double.parseDouble(txtValorTotal.getText()) + valorMulta;
                    atendimento.setValorTotal(valor);
                    txtValorTotal.setText(valor.toString());
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "Opção inválida");
            }


            if (radioCliente.isSelected()) {
                new AtendimentoDAO().alterarLocacaoCliente(atendimento);
            } else {
                new AtendimentoDAO().alterarLocacaoDependente(atendimento);
            }

            JOptionPane.showMessageDialog(this, "Operação efetuada com sucesso!");

            //limpar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro" + e.getMessage());
        }

    }//GEN-LAST:event_btnSalvarActionPerformed


