    private void carregarTela(Atendimento atendimento) throws ParseException, Exception {

        txtId.setText(atendimento.getId() + "");

        if (atendimento.getCliente_id() != 0) {
            txtNome.setText(new NCliente().consultar(atendimento.getCliente_id()).getNome());
        }

        if (atendimento.getDependente_id() != 0) {
            txtNome.setText(new NDependente().consultar(atendimento.getDependente_id()).getNome());
        }

        txtAtendente.setText(new NFuncionario().consultar(atendimento.getFuncionario_id()).getNome());
        txtTitulo.setText(new NTitulo().consultar(atendimento.getTitulo_id()).getNome());
        txtQuantidade.setText(atendimento.getQuantidadeMidias() + "");
        txtMidia.setText(new NMidia().consultar(atendimento.getMidia_id()).getTipoMidia());

        Midia midia = new MidiaDAO().consultarPeloTitulo(new TituloDAO().pegaId(new NTitulo().consultar(atendimento.getTitulo_id()).getNome()), new NMidia().consultar(atendimento.getMidia_id()).getTipoMidia());

        if (atendimento.isStatus() == true) {
            txtStatus.setText("ATIVO");
        } else {
            txtStatus.setText("INATIVO");
        }

        txtDuracao.setText(atendimento.getDuracao() + "");

        SimpleDateFormat in = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat out = new SimpleDateFormat("dd/MM/yyyy");
        String result = out.format(in.parse(atendimento.getDataEntrada().toString()));

        txtDataEntrada.setText(result);

        txtValorTotal.setText(atendimento.getValorTotal() + "");

        btnExcluir.setEnabled(true);

    }

