    private void carregarTabela() {
        try {

            Vector<String> cabecalho = new Vector();
            cabecalho.add("Id");
            cabecalho.add("Nome");
            cabecalho.add("Cargo");

            Vector detalhe = new Vector();

            for (Funcionario funcionario : new NFuncionario().listar()) {
                Vector<String> linha = new Vector();

                linha.add(funcionario.getId() + "");
                linha.add(funcionario.getNome());
                linha.add(funcionario.getCargo());
                detalhe.add(linha);

            }

            tblFuncionarios.setModel(new DefaultTableModel(detalhe, cabecalho));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }
