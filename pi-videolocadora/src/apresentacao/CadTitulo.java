    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        try {

            TituloDAO persistencia = new TituloDAO();

            Titulo titulo = new Titulo();

            if (!txtId.getText().equals("")) {
                titulo.setId(Integer.parseInt(txtId.getText()));
            }
            
            titulo.setNome(txtNome.getText());
            titulo.setGenero(txtGenero.getText());
            titulo.setDiretor(txtDiretor.getText());
            titulo.setAtor(txtAtor.getText());
            titulo.setDisponibilidade(true);

            //Data
            String data = txtLancamento.getText();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            Date data_nascimento = formato.parse(data);
            titulo.setDataLancamento(data_nascimento);
            titulo.setAtendimento_id(0);
            
            new NTitulo().salvar(titulo);

            JOptionPane.showMessageDialog(this, "Operação efetuada com sucesso!");

            limpar();
            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        try {
            
            int resposta = JOptionPane.showConfirmDialog(this,
                    "Deseja realmente excluir o Título?",
                    "Video Locadora",
                    JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {
                new NTitulo().excluir(Integer.parseInt(txtId.getText()));
                limpar();
                JOptionPane.showMessageDialog(this, 
                        "Exclusão efetuada com sucesso!");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

