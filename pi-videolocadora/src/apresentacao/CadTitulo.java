       private void limpar() {

        txtId.setText("");
        txtNome.setText("");
        txtGenero.setText("");
        txtDiretor.setText("");
        txtAtor.setText("");
        txtLancamento.setText("");

        btnExcluir.setEnabled(false);

    }
    
    private void carregarTela(Titulo titulo) throws ParseException {

        txtId.setText(titulo.getId() + "");
        txtNome.setText(titulo.getNome());
        txtGenero.setText(titulo.getGenero());
        txtDiretor.setText(titulo.getDiretor());
        txtAtor.setText(titulo.getAtor());
        
        SimpleDateFormat in = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat out = new SimpleDateFormat("dd/MM/yyyy");
        String result = out.format(in.parse(titulo.getDataLancamento().toString()));
        
        txtLancamento.setText(result);

        btnExcluir.setEnabled(true);

    }

