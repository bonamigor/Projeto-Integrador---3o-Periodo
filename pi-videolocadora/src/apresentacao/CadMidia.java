    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        try {

            Midia midia = new Midia();

            if (!txtId.getText().equals("")) {
                midia.setId(Integer.parseInt(txtId.getText()));
            }
            
            midia.setPreco(Double.parseDouble(txtPreco.getText()));
            midia.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            midia.setTipoMidia(txtTipo.getText());
            midia.setTitulo_id(new MidiaDAO().pegaId(txtTitulo.getText()));
            midia.setDisponibilidade(true);
            
            new NMidia().salvar(midia);

            JOptionPane.showMessageDialog(this, "Operação efetuada com sucesso!");

            limpar();
            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

