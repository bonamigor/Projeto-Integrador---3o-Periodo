       private void limpar() {

        txtId.setText("");
        txtPreco.setText("");
        txtQuantidade.setText("");
        txtTipo.setText("");

        btnExcluir.setEnabled(false);

    }
    
    private void carregarTelaMidia(Midia midia) throws ParseException {

        txtId.setText(midia.getId() + "");
        txtQuantidade.setText(midia.getQuantidade() + "");
        txtPreco.setText(midia.getPreco() + "");
        txtTipo.setText(midia.getTipoMidia());
        txtTitulo.setText(midia.getTitulo_id() + "");

        btnExcluir.setEnabled(true);

    }
    
    private void carregarTelaTitulo(Titulo titulo) throws ParseException {

        txtTitulo.setText(titulo.getNome());

        btnExcluir.setEnabled(true);

    }

